
package MODEL;

public class Producto {
    private int idProducto;
    private String Nombre;
    private float costo;
    private float precio;
    private int existencia;
    private String foto;
    private String Descripcion;

    public Producto(int idProducto, String Nombre, float costo, float precio, int existencia, String foto, String Descripcion) {
        this.idProducto = idProducto;
        this.Nombre = Nombre;
        this.costo = costo;
        this.precio = precio;
        this.existencia = existencia;
        this.foto = foto;
        this.Descripcion = Descripcion;
       
    }
    
   
    
    
}
